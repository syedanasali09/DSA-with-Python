class Person:         # class created
      def __init__ (self, name, age, country, city):       #constructor function built in (self)parameter
          self.name = name
          self.age = age
          self.country = country
          self.city = city

p = Person('Anas','21','pakistan', 'karachi')   #object
print(p.name)   
print(p.age)
print(p.country)
print(p.city)



      
